# 1..10 --> 10 is inclusive
for i in 1..10
  puts "i is #{i}"
end

puts "----END OF LOOP----"

# 1...10 --> 10 is exclusive
for i in 1...10
  puts "i is #{i}"
end

puts "----END OF LOOP----"

for i in "a".."z"
  puts "i is #{i}"
end

puts "----END OF LOOP----"

for i in "aa".."zz"
  puts "i is #{i}"
end

puts "----END OF LOOP----"